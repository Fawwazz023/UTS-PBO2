    private void btnSmpnStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSmpnStockActionPerformed
        try {
            if(txtIdBarang.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "ID Barang Belum Diisi");
                txtIdBarang.requestFocus();
            } else if(txtNamaBarang.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Nama Barang Belum Diisi");
                txtNamaBarang.requestFocus();
            } else if(txtJenisBarang.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Jenis Barang Belum Diisi");
                txtJenisBarang.requestFocus();
            } else if(txtDeskripsi.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Deskripsi Belum Diisi");
                txtDeskripsi.requestFocus();
            } else if(txtStock.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Stock Belum Diisi");
                txtStock.requestFocus();
            } else if(txtSatuan.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Satuan Belum Diisi");
                txtSatuan.requestFocus();
            } else {
                // Sesuai pola, memanggil simpan02 untuk keamanan dan cek duplikasi
                mycode.simpanStock02(txtIdBarang.getText(), txtNamaBarang.getText(),
                                     txtJenisBarang.getText(), txtDeskripsi.getText(),
                                     txtStock.getText(), txtSatuan.getText());

                mycode.tampilDataStock(tabelStock, "SELECT*FROM stock");
                if (mycode.duplikasi == true){
                    txtNamaBarang.setText(mycode.CEK_NAMABARANG);
                    txtJenisBarang.setText(mycode.CEK_JENISBARANG);
                    txtDeskripsi.setText(mycode.CEK_DESKRIPSI);
                    txtStock.setText(mycode.CEK_STOCK);
                    txtSatuan.setText(mycode.CEK_SATUAN);
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnSmpnStockActionPerformed

