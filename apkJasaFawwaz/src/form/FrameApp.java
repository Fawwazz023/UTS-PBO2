    private void btnSmpnPinjamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSmpnPinjamActionPerformed
        try {
            if(txtIdPeminjaman.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "ID Peminjaman Belum Diisi");
                txtIdPeminjaman.requestFocus();
            } else if(txtIdBarangPinjam.getText().isEmpty()){ // Asumsi nama field beda
                JOptionPane.showMessageDialog(null, "ID Barang Belum Diisi");
                txtIdBarangPinjam.requestFocus();
            } else if(txtTanggalPinjam.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Tanggal Pinjam Belum Diisi");
                txtTanggalPinjam.requestFocus();
            } else if(txtQtyPinjam.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Qty Belum Diisi");
                txtQtyPinjam.requestFocus();
            } else if(txtPeminjam.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Peminjam Belum Diisi");
                txtPeminjam.requestFocus();
            } else if(txtKeperluanPinjam.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Keperluan Belum Diisi");
                txtKeperluanPinjam.requestFocus();
            } else if(txtStatusPinjam.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Status Belum Diisi");
                txtStatusPinjam.requestFocus();
            } else if(txtJumlahKembali.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Jumlah Kembali Belum Diisi");
                txtJumlahKembali.requestFocus();
            } else if(txtTanggalKembali.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Tanggal Kembali Belum Diisi");
                txtTanggalKembali.requestFocus();
            } else {
                mycode.simpanPeminjaman02(txtIdPeminjaman.getText(), txtIdBarangPinjam.getText(),
                                         txtTanggalPinjam.getText(), txtQtyPinjam.getText(),
                                         txtPeminjam.getText(), txtKeperluanPinjam.getText(),
                                         txtStatusPinjam.getText(), txtJumlahKembali.getText(),
                                         txtTanggalKembali.getText());

                mycode.tampilDataPeminjaman(tabelPinjam, "SELECT*FROM peminjaman");

                if (mycode.duplikasi == true){
                    txtIdBarangPinjam.setText(mycode.CEK_IDBARANG_PINJAM);
                    txtTanggalPinjam.setText(mycode.CEK_TANGGALPINJAM);
                    txtQtyPinjam.setText(mycode.CEK_QTY_PINJAM);
                    txtPeminjam.setText(mycode.CEK_PEMINJAM);
                    txtKeperluanPinjam.setText(mycode.CEK_KEPERLUAN_PINJAM);
                    txtStatusPinjam.setText(mycode.CEK_STATUS_PINJAM);
                    txtJumlahKembali.setText(mycode.CEK_JUMLAHKEMBALI);
                    txtTanggalKembali.setText(mycode.CEK_TANGGALKEMBALI);
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnSmpnPinjamActionPerformed

